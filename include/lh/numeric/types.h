#ifndef LH_NUMERIC_TYPES_H
#define LH_NUMERIC_TYPES_H

/**
 * @brief A signed integer with the C short representation.
 */
typedef short lh_short_t;
/**
 * @brief A signed integer with the C int representation.
 */
typedef int lh_int_t;
/**
 * @brief A signed integer with the C long representation.
 */
typedef long lh_long_t;
/**
 * @brief A signed integer with the C long long representation.
 */
typedef long long lh_llong_t;

/**
 * @brief An unsigned integer with the C short representation.
 */
typedef unsigned short lh_ushort_t;
/**
 * @brief An unsigned integer with the C int representation.
 */
typedef unsigned int lh_uint_t;
/**
 * @brief An unsigned integer with the C long representation.
 */
typedef unsigned long lh_ulong_t;
/**
 * @brief An unsigned integer with the C long long representation.
 */
typedef unsigned long long lh_ullong_t;

/**
 * @brief An explicitly signed C short integer.
 */
typedef signed short lh_sshort_t;
/**
 * @brief An explicitly signed C int integer.
 */
typedef signed int lh_sint_t;
/**
 * @brief An explicitly signed C long integer.
 */
typedef signed long lh_slong_t;
/**
 * @brief An explicitly signed C long long integer.
 */
typedef signed long long lh_sllong_t;

#endif /* LH_NUMERIC_TYPES_H */
